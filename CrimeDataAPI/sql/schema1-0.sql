    REPORT_YEAR INT NOT NULL,
    REPORT_MONTH VARCHAR(20) NOT NULL,
    REPORT_DAY INT NOT NULL,
    REPORT_DOY INT NOT NULL,
    REPORT_DOW VARCHAR(20) NOT NULL,
    REPORT_HOUR INT NOT NULL,
    OCC_YEAR INT NOT NULL,
    OCC_MONTH VARCHAR(20) NOT NULL,
    OCC_DAY INT NOT NULL,
    OCC_DOY INT NOT NULL,
    OCC_DOW VARCHAR(20) NOT NULL,
    OCC_HOUR INT NOT NULL,
    DIVISION VARCHAR(10) NOT NULL,
    LOCATION_TYPE TEXT NOT NULL,
    PREMISES_TYPE VARCHAR(50) NOT NULL,
    UCR_CODE VARCHAR(10) NOT NULL,
