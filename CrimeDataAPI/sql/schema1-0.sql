    MCI_CATEGORY VARCHAR(50) NOT NULL,
    HOOD_158 VARCHAR(50) NOT NULL,
    NEIGHBOURHOOD_158 VARCHAR(50) NOT NULL,
    HOOD_140 VARCHAR(50) NOT NULL,
    NEIGHBOURHOOD_140 VARCHAR(50) NOT NULL,
    LONG_WGS84 DOUBLE PRECISION NOT NULL,
    LAT_WGS84 DOUBLE PRECISION NOT NULL,
